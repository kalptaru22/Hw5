package JavaWeek5;

public class QueNine {
    public static void main(String[] args)
    {
        int a= (-5 + 8 * 6);// here a is a variable
        int b=( (55+9) % 9);// here b is a variable
        int c= (20 + -3*5 / 8);// here c is a variable
        int d= (5 + 15 / 3 * 2 - 8 % 3);// here d is a variable
        System.out.println (a);
        System.out.println (b);
        System.out.println (c);
        System.out.println (d);
    }
}
